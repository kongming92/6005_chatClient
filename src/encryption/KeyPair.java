package encryption;

import java.math.BigInteger;

public class KeyPair {
	private final BigInteger first;
	private final BigInteger second;
	public KeyPair(BigInteger first, BigInteger second){
		this.first=first;
		this.second = second;
	}
	
	public BigInteger first(){
		return new BigInteger(first.toString());
	}
	
	public BigInteger second(){
		return new BigInteger(second.toString());
	}
	
	public String toString(){
		return first.toString()+" "+second.toString();
	}
}
