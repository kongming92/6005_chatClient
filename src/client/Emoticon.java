package client;

public enum Emoticon {
    
    NONE (""), 
    COLON_CLOSE (":)"), 
    COLON_D (":D"), 
    COLON_O (":O"), 
    COLON_OPEN (":("), 
    COLON_P (":P"), 
    SEMI_CLOSE (";)"), 
    EIGHT_CLOSE ("8)");
    
    private final String style;
    private Emoticon(String s) {
        this.style = s;
    }
    public String getStyle() {
        return this.style;
    }

}
